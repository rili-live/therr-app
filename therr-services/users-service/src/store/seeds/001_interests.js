exports.seed = async (knex) => {
    // Insert all interests with ON CONFLICT DO NOTHING to gracefully handle existing data
    const results = await Promise.all(
        interests.map((interest) => knex.raw(`
                INSERT INTO main.interests (id, tag, "categoryKey", "displayNameKey", emoji, "iconGroup", "iconId", "iconColor")
                VALUES (?, ?, ?, ?, ?, ?, ?, ?)
                ON CONFLICT (id) DO NOTHING
            `, [
            interest.id,
            interest.tag,
            interest.categoryKey,
            interest.displayNameKey,
            interest.emoji,
            interest.iconGroup,
            interest.iconId,
            interest.iconColor,
        ])),
    );

    const inserted = results.filter((r) => r.rowCount > 0).length;
    const skipped = results.length - inserted;

    // eslint-disable-next-line no-console
    console.log(`Interests seed complete: ${inserted} inserted, ${skipped} skipped (already exist)`);
};
